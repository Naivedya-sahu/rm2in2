# Letter spacing
LETTER_SPACING = 15  # pixels between letters
WORD_SPACING = 40    # pixels between words


def get_letter_strokes(char):
    """Get stroke data for a character"""
    return LETTERS.get(char.upper(), LETTERS.get(char, None))


def get_letter_width(char):
    """Get width of a character"""
    if char == ' ':
        return WORD_SPACING

    strokes = get_letter_strokes(char)
    if not strokes:
        return 30  # default width

    # Find max X in all strokes
    max_x = 0
    for stroke in strokes:
        for x, y in stroke:
            max_x = max(max_x, x)

    return max_x + LETTER_SPACING


